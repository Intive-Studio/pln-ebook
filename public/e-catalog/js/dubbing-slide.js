var audio22 = document.getElementById("audio22");
var isPlaying = false;

function play22() {
    if (isPlaying) {
        audio22.pause();
        $('#play22').attr('class', 'fa fa-play-circle fa-3x');
    } else {
        audio22.play();
        $('#play22').attr('class', 'fa fa-pause-circle fa-3x');
    }
};
audio22.onplaying = function () {
    isPlaying = true;
};
audio22.onpause = function () {
    isPlaying = false;
};

