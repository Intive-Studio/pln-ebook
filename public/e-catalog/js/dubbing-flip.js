// dubbing page2
var audio2 = document.getElementById("audio2");
var isPlaying = false;

function play2() {
    if (isPlaying) {
        audio2.pause();
        $('#play2').attr('class', 'fa fa-play-circle fa-3x');
    } else {
        audio2.play();
        $('#play2').attr('class', 'fa fa-pause-circle fa-3x');
    }
};
audio2.onplaying = function () {
    isPlaying = true;
};
audio2.onpause = function () {
    isPlaying = false;
};

